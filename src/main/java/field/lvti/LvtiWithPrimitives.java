package field.lvti;

public class LvtiWithPrimitives {
  public static void main(String[] args) {
    char charValue = 'a';
    boolean booleanValue = true;
    byte byteValue = 1;
    short shortValue = 1;
    int intValue = 1;
    long longValue = 1;
    float floatValue = 1.0f;
    double doubleValue = 1.0;

    var lvtiChar = 'a';
    var lvtiBoolean = true;
    var lvtiByte = (byte) 1;
    var lvtiShort = (short) 1;
    var lvtiInt = 1;
    var lvtiLong = 1L;
    var lvtiFloat = 1.0F;
    var lvtiDouble = 1.0D;
  }
}
